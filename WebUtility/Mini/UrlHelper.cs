using System.Text;
using Cvv.WebUtility.Mvc;

namespace Cvv.WebUtility.Mini
{
    public class UrlHelper
    {
        internal static string GetUrlPath(string relativePath, string pathInfo)
        {
            string path = relativePath.Substring(2);

            return path + pathInfo;
        }

        internal static string GetUrlPathWithoutExtension(string relativePath)
        {
            string path = relativePath.Substring(2);

            if (WebAppConfig.Extension.Length > 0 && path.EndsWith(WebAppConfig.Extension))
            {
                path = path.Remove(path.Length - WebAppConfig.Extension.Length);
            }
            else
            {
                int len = path.LastIndexOf('.');

                if (len != -1)
                    path = path.Remove(len);
            }

            return path.Replace("-", string.Empty);
        }

        internal static string GetUrlWithoutPathInfo(string rawUrl, string pathInfo)
        {
            if (string.IsNullOrEmpty(pathInfo))
                return rawUrl;
            else
                return rawUrl.Remove(rawUrl.LastIndexOf(pathInfo), pathInfo.Length);
        }

        public static string UrlEncode(string str)
        {
            if (str == null)
            {
                return null;
            }
            return UrlEncode(str, Encoding.UTF8);
        }

        public static string UrlEncode(string str, Encoding e)
        {
            if (str == null)
            {
                return null;
            }
            return Encoding.ASCII.GetString(UrlEncodeToBytes(str, e));
        }

        private static byte[] UrlEncodeBytesToBytesInternal(byte[] bytes, int offset, int count, bool alwaysCreateReturnValue)
        {
            int num = 0;
            int num2 = 0;
            for (int i = 0; i < count; i++)
            {
                char ch = (char)bytes[offset + i];
                if (ch == ' ')
                {
                    num++;
                }
                else if (!IsSafe(ch))
                {
                    num2++;
                }
            }
            if ((!alwaysCreateReturnValue && (num == 0)) && (num2 == 0))
            {
                return bytes;
            }
            byte[] buffer = new byte[count + (num2 * 2)];
            int num4 = 0;
            for (int j = 0; j < count; j++)
            {
                byte num6 = bytes[offset + j];
                char ch2 = (char)num6;
                if (IsSafe(ch2))
                {
                    buffer[num4++] = num6;
                }
                else if (ch2 == ' ')
                {
                    buffer[num4++] = 0x2b;
                }
                else
                {
                    buffer[num4++] = 0x25;
                    buffer[num4++] = (byte)IntToHex((num6 >> 4) & 15);
                    buffer[num4++] = (byte)IntToHex(num6 & 15);
                }
            }
            return buffer;
        }

        private static byte[] UrlEncodeToBytes(string str, Encoding e)
        {
            if (str == null)
            {
                return null;
            }
            byte[] bytes = e.GetBytes(str);
            return UrlEncodeBytesToBytesInternal(bytes, 0, bytes.Length, false);
        }

        private static bool IsSafe(char ch)
        {
            if ((((ch >= 'a') && (ch <= 'z')) || ((ch >= 'A') && (ch <= 'Z'))) || ((ch >= '0') && (ch <= '9')))
            {
                return true;
            }
            switch (ch)
            {
                case '\'':
                case '(':
                case ')':
                case '*':
                case '-':
                case '.':
                case '_':
                case '!':
                    return true;
            }
            return false;
        }

        private static char IntToHex(int n)
        {
            if (n <= 9)
            {
                return (char)(n + 0x30);
            }
            return (char)((n - 10) + 0x61);
        }
    }
}
